public class P16 {
    public static void main(String[] args) {

        //Problema 16 - Sa se calculeze factorialul unui numar a.

        int b=6;
        int result=1;
        for(int i=1;i<=b;i++){

            result=result*i;
        }
        System.out.println("The result is: " +result);
    }
}
