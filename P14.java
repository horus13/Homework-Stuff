public class P14 {
    public static void main(String[] args) {

        //Problema 14 - Se da un numar. Sa se afiseze ‘true' daca numarul apartine intervalului [9,24]. Altfel sa se afiseze ‘false'.

        int n=10;
        if (n<=24 && n>=9)
            System.out.println(n+ " is part of the interval so the statement is " +true);
        else
            System.out.println(n+ " is part of the interval so the statement is " +false);

    }
}
